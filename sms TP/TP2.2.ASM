JMP RESTORE;
DB FA; DISPLAY NUMBER 0
DB 0A; DISPLAY NUMBER 1
DB B6; DISPLAY NUMBER 2
DB 9E; DISPLAY NUMBER 3
DB 4E; DISPLAY NUMBER 4
DB DC; DISPLAY NUMBER 5
DB FC; DISPLAY NUMBER 6
DB 8A; DISPLAY NUMBER 7
DB FE; DISPLAY NUMBER 8
DB DE; DISPLAY NUMBER 9

RESTORE:
MOV AL, 00
MOV BL, 00

DISPLAY_NUMBER:
MOV [B0], DL
MOV BL, [B0]

ADD BL, 02
MOV AL, [BL]

INC AL
OUT 02

MOV BL, [BF]
DIV BL, 0A
ADD BL, 02
MOV AL, [BL]
OUT 02

INC DL
CMP DL, A
JNZ DISPLAY_NUMBER
END